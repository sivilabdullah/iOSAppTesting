import Foundation

class MathFunc {
    func addIntegers(x: Int, y: Int) -> Int {
        return x + y
    }
    func minusIntegers(x: Int, y: Int) -> Int {
        return x -  y
    }
    func MultiplyIntegers(x: Int, y: Int) -> Int {
        return x * y
    }
}
